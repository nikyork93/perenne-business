#!/usr/bin/env bash
set -e
cd "$(dirname "$0")"

echo "═══════════════════════════════════════════════════════════"
echo "v42 — Endpoint redeem per attivazione codici monouso iOS"
echo "═══════════════════════════════════════════════════════════"
echo ""
echo "Cosa fa:"
echo ""
echo "  1. NUOVO endpoint: POST /api/team/[code]/redeem"
echo "     - Body: { deviceId?: string }"
echo "     - 200 + design payload  → codice valido + redemption riuscita"
echo "     - 400 invalid_format   → codice malformato"
echo "     - 404 not_found        → codice non esistente"
echo "     - 409 already_redeemed → codice gia' usato (NotebookCode)"
echo "     - 410 invalid          → codice revocato/scaduto/inattivo"
echo ""
echo "  2. NotebookCode (Stripe-issued): MONOUSO. Una sola redemption"
echo "     mai piu'. Lo stato passa AVAILABLE -> CLAIMED al primo POST."
echo "     Idempotency: se lo stesso deviceId riprova, server risponde"
echo "     200 con il payload (no errore). Device diverso -> 409."
echo ""
echo "  3. LegacyTeamCode (admin-created, trade show): MULTI-REDEMPTION"
echo "     server-side. Il client iOS impone client-side 'un quaderno"
echo "     per codice'. Server non blocca."
echo ""
echo "  4. GET /api/team/[code] esistente NON tocco. Continua a"
echo "     funzionare per back-compat con vecchie iOS app."
echo ""
echo "  5. Per i tuoi test STLV: i codici esistenti restano AVAILABLE"
echo "     (opzione B della discussione). Quando testi STLV per la"
echo "     prima volta da una iOS aggiornata, lo claimi sul tuo"
echo "     iPad. Per testarlo di nuovo dovrai cancellarlo dal portale"
echo "     e ricrearlo, oppure resettare a mano lo status nel DB:"
echo "       UPDATE \"NotebookCode\" SET status='AVAILABLE',"
echo "         \"claimedAt\"=NULL, \"claimedDeviceId\"=NULL,"
echo "         \"claimedIpAddress\"=NULL WHERE code='STLV';"
echo ""
echo "Niente schema migration: NotebookCode ha gia' i campi"
echo "claimedAt/claimedDeviceId/claimedIpAddress."
echo ""
echo "Premi INVIO per continuare, Ctrl+C per annullare."
read -r _

echo "═══════════════════════════════════════════════════════════"
echo "STEP 1/2: copia file"
echo "═══════════════════════════════════════════════════════════"
mkdir -p "app/api/team/[code]/redeem"
cp -v "_v42_payload/app/api/team/[code]/redeem/route.ts" "app/api/team/[code]/redeem/route.ts"

rm -rf _v42_payload

echo ""
echo "═══════════════════════════════════════════════════════════"
echo "STEP 2/2: git commit + push"
echo "═══════════════════════════════════════════════════════════"
git add -A
git commit -m "v42: POST /api/team/[code]/redeem one-shot redemption endpoint for iOS multi-code support"
git push

